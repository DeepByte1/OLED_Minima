class OLED_Minima {
  public:
    OLED_Minima(uint8_t addr = 0x3C) : _addr(addr) {}

    void sendCommand(uint8_t cmd) {
      Wire.beginTransmission(_addr);
      Wire.write(0x00); Wire.write(cmd);
      Wire.endTransmission();
    }

    void begin(uint8_t Model, bool X_Mirror, bool Y_Mirror) {
      Wire.begin();
      sendCommand(0xAE); sendCommand(0xD5); sendCommand(0x80);
      sendCommand(0xA8); sendCommand(0x3F); sendCommand(0xD3); sendCommand(0x00);
      sendCommand(0x40); sendCommand(0x8D); sendCommand(0x14); sendCommand(0x20); sendCommand(0x00);
      if (X_Mirror) sendCommand(0xA1); else sendCommand(0xA0);
      if (Y_Mirror) sendCommand(0xC8); else sendCommand(0xC0);
      sendCommand(0xDA);
      if (Model == 0) sendCommand(0x12);
      else if (Model == 1) sendCommand(0x22);
      else sendCommand(0x02);
      sendCommand(0x81); sendCommand(0xCF); sendCommand(0xAF);
      clearAll();
    }

    void clearAll() {
      sendCommand(0x21); sendCommand(0); sendCommand(127);
      sendCommand(0x22); sendCommand(0); sendCommand(7);
      for (uint16_t i = 0; i < 1024; i++) {
        Wire.beginTransmission(_addr); Wire.write(0x40);
        for (uint8_t j = 0; j < 16; j++) { Wire.write(0x00); i++; }
        i--; Wire.endTransmission();
      }
      setCursor(0, 0);
    }

    void setCursor(uint8_t x, uint8_t y) { cursorX = x; cursorY = y; }

    void printChar(char c) {
      if (c < 32 || c > 90) {
        if (c >= 'a' && c <= 'z') c -= 32;
        else c = 32;
      }
      uint16_t offset = (uint16_t)(c - 32) * _FONT_W;

      sendCommand(0x21); sendCommand(cursorX); sendCommand(cursorX + _FONT_W - 1);
      sendCommand(0x22); sendCommand(cursorY); sendCommand(cursorY);
      Wire.beginTransmission(_addr);
      Wire.write(0x40);
      for (uint8_t i = 0; i < _FONT_W; i++) Wire.write(pgm_read_byte(&font[offset + i]));
      Wire.endTransmission();

      cursorX += CHAR_SPACING ? (_FONT_W + 1) : _FONT_W;
      if (cursorX >= (128 - _FONT_W)) println();
    }

    void print(const char* s) { while (*s) printChar(*s++); }
    void println(const char* s) { print(s); println(); }
    void print(int n) { char b[7]; itoa(n, b, 10); print(b); }
    void println(int n) { print(n); println(); }
    void println() { cursorX = 0; cursorY++; if (cursorY > 7) cursorY = 0; }
    
    void print(const __FlashStringHelper *ifsh) {
      PGM_P p = reinterpret_cast<PGM_P>(ifsh);
      char c;
      while ((c = pgm_read_byte(p++)) != 0) printChar(c);
    }
    void println(const __FlashStringHelper *ifsh) { print(ifsh); println(); }

    void drawBitmap(const uint8_t* b, uint8_t x, uint8_t y, uint8_t w, uint8_t h) {
      uint8_t hp = (h + 7) / 8; uint8_t yp = y / 8;
      sendCommand(0x21); sendCommand(x); sendCommand(x + w - 1);
      sendCommand(0x22); sendCommand(yp); sendCommand(yp + hp - 1);
      uint16_t total = (uint16_t)w * hp;
      for (uint16_t i = 0; i < total; i++) {
        if (i % 16 == 0) { if (i > 0) Wire.endTransmission(); Wire.beginTransmission(_addr); Wire.write(0x40); }
        Wire.write(pgm_read_byte(&b[i]));
      }
      Wire.endTransmission();
    }

    bool CHAR_SPACING = true;
    uint8_t DISPLAY_WIDTH = 128;
    uint8_t DISPLAY_HEIGHT = 64;

  private:
    uint8_t _addr;
    uint8_t cursorX = 0;
    uint8_t cursorY = 0;
};

